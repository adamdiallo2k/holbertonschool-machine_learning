import numpy as np


def np_matmul(mat1, mat2):
    """
    Perform matrix multiplication on two numpy arrays.
    
    :param mat1: First numpy array
    :param mat2: Second numpy array
    :return: Resultant numpy array after matrix multiplication
    """
    return np.matmul(mat1, mat2)